								<a name ="blog5"><h1 class="<?php echo $colClass; ?>">Barefoot Running for the Neutral Runner</h1></a>
								<br/>
								<!--<p class="<?php echo $colClass; ?>">23-May-2011</p><br/>--><p>
								<p>When I refer to barefoot running, I personally do not literally mean running with no shoes on, it quite simply is 
								not practical in the society in which we live for one, and more importantly for me, has far too many health and medical 
								implications. Bruised metatarsals we do not want! I simply mean running in a minimalist trainer that is not as built up 
								as many on the market today are.</p>
								<br/>

								<p>Barefoot running is a much debated subject at the moment. It has lead me to thinking more about what we put on our 
								feet as runners and wondering if the science behind many running shoes has become too complex. Are trainers actually taking
								away from how we should run naturally. For the neutral to mild pronator in particular would running more closely to the way 
								our foot naturally moves without being constricted too much by shoe for example, lead to more sustainable running programmes, 
								i.e less injury in fact! Today, too many neutral to mild pronation runners are pointed towards more shoe than they actually need.</p>
								<br/>

								<p>I must admit - I am not one for all this five finger malarky - in fact, I find it a bit strange if I am honest. The majority 
								of us, quite simply, have worn shoes for too long to start going that minimalist and we would risk introducing a host of problems. 
								And as advocates of the five fingers suggest we need to learn to run a new way to manage the shoe, I do not think this is realistic. 
								Most of us are too set in our ways! Our bodies have adapted over the years to our own particular styles, and they manage our little 
								quirks quite efficiently, if not as "biomechanically" correct as some sports scientist might like!</p>
								<br/>

								<p>Many trainers recommended for the neutral to mild over-pronator have a great deal of bulk around the heel area, lack flexibility 
								and are very built up often on the inner side of the shoe - so I think a happy medium is required. Some of the latest shoes on the 
								market offer new options to the neutral runner. They have the benefit of a flexible sole, along with cushioning and allow the foot 
								to run freely, so at the same time building up strength.</p>
								<br/>

								<p>With some of my clients, I feel their trainers are limiting their progression in running. So I encourage them to introduce 
								minimalist running shoes gradually into their running programmes and build up the mileage slowly with caution, I will also incorporate 
								a strength plan that runs alongside the transition and then I can ensure they will have continuity of training... Important to all runners!</p>
								<br/>

								<p>Remember each individual is different and "barefoot" running will not be for everyone. Go wisely - and ultimately do what is right 
								for you and your feet.</p>
								<br/>

								<p>Happy running!</p>
